package br.com.ada.aula1;

public class Principal {

    public static void main(String[] x) {

        // tipos primitivos = (tipos pre definidos pelo java)

        // numeros inteiros
        byte a; // -128 ate 128 (8-bit = 1 byte)
        short b; // -32.768 ate 32.767 (16-bit = 2 byte)
        int c; // -2,147 BI até 2,147 BI (32-bit = 4 byte)
        long d; // -9 qui até 9 qui (64 bit = 8 bytes)

        // numeros de ponto flutuante
        float e; // 6 a 7 casas decimais
        double f; // 15 casas decimais

        // flag
        boolean g; // true / false

        // charactere
        char h; // unico charactere asciii aceita aprox. 65k valores
    }

}
